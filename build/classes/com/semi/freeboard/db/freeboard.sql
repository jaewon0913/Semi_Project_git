DROP TABLE FREEBOARD;
DROP SEQUENCE FREEBOARDSEQ;

CREATE SEQUENCE FREEBOARDSEQ;

CREATE TABLE FREEBOARD(
	FREEBOARD_NO NUMBER PRIMARY KEY,
	FREEBOARD_ID VARCHAR2(300) REFERENCE MEMBER (MEMBER_ID) NOT NULL,
	FREEBOARD_TITLE VARCHAR2(1000) NOT NULL,
	FREEBOARD_CONTENT VARCHAR2(4000) NOT NULL,
	FREEBOARD_REGDATE DATE NOT NULL,
	FREEBOARD_GROUPNO NUMBER NOT NULL,
	FREEBOARD_GROUPORDER NUMBER NOT NULL,
	FREEBOARD_TITLETAB NUMBER NOT NULL,
	FREEBOARD_DELENABLED VARCHAR2(2) NOT NULL
);

