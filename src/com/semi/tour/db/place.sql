
DROP SEQUENCE PLACESEQ;
DROP TABLE PLACE;

CREATE SEQUENCE PLACESEQ;


--번호, 도시이름(광역시&도), 관광지이름, 관광지 연락처, 관광지 주소, 위도, 경도, 게시물 조회수, 
--게시물 좋아요버튼...ㅋㅋ할 수 있으면 좋아요순 정렬도!!, 가이드, 관광지 정보(써머리)
CREATE TABLE PLACE(
	
	PLACE_NO NUMBER PRIMARY KEY,
	CITY_NAME VARCHAR2(300),
	PLACE_NAME VARCHAR2(300),
	PHON VARCHAR2(300),
	ADRESS VARCHAR2(300),
	LATITUDE VARCHAR2(300),
	LONGITUDE VARCHAR2(300),
	PLACE_HITS NUMBER,
	PLACE_LIKE NUMBER,
	GUIDE CLOB,
	PLACE_INFO CLOB

);

