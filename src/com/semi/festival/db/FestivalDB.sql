DROP TABLE FESTIVAL_DB;
DROP SEQUENCE FESTIVAL_SEQ;

CREATE SEQUENCE FESTIVAL_SEQ;

CREATE TABLE FESTIVAL_DB(

FESTIVAL_SEQ NUMBER PRIMARY KEY,
FESTIVAL_NAME VARCHAR2(1000) NOT NULL,
FESTIVAL_PLACE VARCHAR2(4000) NOT NULL,
FESTIVAL_START VARCHAR2(1000) NOT NULL,
FESTIVAL_END VARCHAR2(1000) NOT NULL,
FESTIVAL_IMG VARCHAR2(4000),
FESTIVAL_CONTENT VARCHAR2(4000),
FESTIVAL_PHONE VARCHAR2(1000),
HOMEPAGE VARCHAR2(4000),
ADDRESS VARCHAR2(4000),
AGENCY VARCHAR2(1500)
);

SELECT FESTIVAL_SEQ, FESTIVAL_NAME,FESTIVAL_PLACE, FESTIVAL_START,FESTIVAL_END,FESTIVAL_IMG,
FESTIVAL_CONTENT,FESTIVAL_PHONE,HOMEPAGE,ADDRESS,AGENCY FROM FESTIVAL_DB;

DELETE FROM FESTIVAL_DB;

SELECT * FROM FESTIVAL_DB WHERE SUBSTR(FESTIVAL_START,6,2) = '01';